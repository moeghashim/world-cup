/** language registry — ltr first, RTL (Arabic) last */
export const LANGS: LangMeta[] = [
  { code: 'en', name: 'English', short: 'EN', dir: 'ltr' },
  { code: 'es', name: 'Español', short: 'ES', dir: 'ltr' },
  { code: 'fr', name: 'Français', short: 'FR', dir: 'ltr' },
  { code: 'pt', name: 'Português', short: 'PT', dir: 'ltr' },
  { code: 'ar', name: 'العربية', short: 'ع', dir: 'rtl', font: 'ar' },
]
export function langMeta(code: string): LangMeta {
  return LANGS.find((l) => l.code === code) ?? LANGS[0]
}

/** translate a key for a language, with {0}/{1}/{2} interpolation + EN fallback */
export function translate(lang: string, key: string, ...args: (string | number)[]): string {
  const d = DICT[lang]
  const s = d && d[key] != null ? d[key] : DICT.en[key] != null ? DICT.en[key] : key
  return s.replace(/\{(\d)\}/g, (m, i) => {
    const v = args[+i]
    return v != null ? String(v) : m
  })
}

/** localized team name for a code */
export function teamName(lang: string, code: string): string {
  if (code === 'Other') return translate(lang, 'other_team')
  const tm = TEAMS[code]
  if (!tm) return code
  if (COUNTRY[lang] && COUNTRY[lang][code]) return COUNTRY[lang][code]
  return tm.n
}
